const input = prompt().toLowerCase()

switch (input) {
  case 'monday':
    console.log('Понедельник');
    break
  case "tuesday":
    console.log('вторник');
    break
  case 'wednesday':
    console.log("среда");
    break
  case 'thursday':
    console.log('четверг');
    break
  case 'friday':
    console.log('пятница');
    break
  case "saturday":
    console.log("суббота");
    break
  case "sunday":
    console.log("воскресенье");
    break
    default:
      console.log("Ошибка");
} 



